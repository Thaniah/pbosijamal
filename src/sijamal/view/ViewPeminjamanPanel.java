    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Pilih lokasi untuk menyimpan file CSV");

        int userSelection = fileChooser.showSaveDialog(this);
        if (userSelection == JFileChooser.APPROVE_OPTION) {
            String filePath = fileChooser.getSelectedFile().getAbsolutePath();
            if (!filePath.toLowerCase().endsWith(".csv")) {
                filePath += ".csv";
            }
            controller.peminjamanToCSV(filePath);
            JOptionPane.showMessageDialog(this, "Data berhasil diekspor ke CSV di lokasi: " + filePath);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow >= 0) {
            String nimPeminjam = (String) jTable1.getValueAt(selectedRow, 0);
            String namaKategori = (String) jTable1.getValueAt(selectedRow, 1);
            String namaAlat = (String) jTable1.getValueAt(selectedRow, 2);
            String tanggalPinjamStr = (String) jTable1.getValueAt(selectedRow, 3); // Ambil tanggal sebagai string
            String waktuPinjamStr = (String) jTable1.getValueAt(selectedRow, 4); // Ambil waktu sebagai string
            int jumlah = (int) jTable1.getValueAt(selectedRow, 5);
            String status = (String) jTable1.getValueAt(selectedRow, 6);
    
            // Konversi tanggal dan waktu dari string ke LocalDate dan LocalTime
            LocalDate tanggalPinjam = LocalDate.parse(tanggalPinjamStr);
            LocalTime waktuPinjam = LocalTime.parse(waktuPinjamStr);
    
            Peminjaman peminjaman = new Peminjaman();
            peminjaman.setNimPeminjam(nimPeminjam);
            peminjaman.setNamaKategori(namaKategori);
            peminjaman.setNamaAlat(namaAlat);
            peminjaman.setTanggalPinjam(tanggalPinjam);
            peminjaman.setWaktuPinjam(waktuPinjam);
            peminjaman.setJumlah(jumlah);
            peminjaman.setStatus(status);

            if ("Diajukan".equals(status)) {
                controller.showTinjauPeminjamanPanel(peminjaman); 
            } else {
                
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked


