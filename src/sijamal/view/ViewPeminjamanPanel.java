    public void displayPeminjaman() {
    List<Peminjaman> peminjamans = controller.getAllPeminjaman();
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); 
    for (Peminjaman p : peminjamans) {
        model.addRow(new Object[]{
            p.getNimPeminjam(),
            p.getNamaKategori(),
            p.getNamaAlat(),
            p.getTanggalPinjam().toString(), // Konversi LocalDate ke String
            p.getWaktuPinjam().toString(),   // Konversi LocalTime ke String
            p.getJumlah(),
            p.getStatus()
        });
    }
}

